#include "libft.h"

int	ft_is_hex_digit(char c)
{
	return (((c >= '0') && c <= '9')
		|| (c >= 'a' && c <= 'f') || (c >= 'A' && c <= 'F'));
}

int	ft_htoi(char s[])
{
	int	i;
	int	n;

	n = 0;
	i = 0;
	if (s[0] == '0' && (s[1] == 'x' || s[1] == 'X'))
		i = 2;
	while (s[i] != '\0')
	{
		if (!ft_is_hex_digit(s[i]))
			return (n);
		n *= 16;
		if (s[i] >= '0' && s[i] <= '9')
			n += s[i] - '0';
		else if (s[i] >= 'a' && s[i] <= 'f')
			n += 10 + (s[i] - 'a');
		else if (s[i] >= 'A' && s[i] <= 'F')
			n += 10 + (s[i] - 'A');
		i++;
	}
	return (n);
}
